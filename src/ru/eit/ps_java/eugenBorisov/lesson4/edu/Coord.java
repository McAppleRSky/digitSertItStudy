package ru.eit.ps_java.eugenBorisov.lesson4.edu;

public class Coord {
    private int y;
    private int x;

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public Coord(int y, int x) {
        this.y = y;
        this.x = x;
    }
}
